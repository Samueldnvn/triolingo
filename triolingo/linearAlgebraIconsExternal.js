// Helper functions
function getLinearAlgebraExternalIcon(term) {
  return linearAlgebraIconsExternal[term] || { type: "emoji", emoji: "❓" };
}

function getLinearAlgebraSVGPath(term) {
  const icon = getLinearAlgebraExternalIcon(term);
  return icon.type === "svg" ? icon.path : null;
}

function getLinearAlgebraIconImage(term) {
  const icon = getLinearAlgebraExternalIcon(term);
  return icon.type === "image" ? icon.url : null;
}